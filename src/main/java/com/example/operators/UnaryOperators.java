package com.example;
public class UnaryOperators {

    public int unaryPlus(int x) {
        return +x;
    }

    public int unaryMinus(int x) {
        return -x;
    }

    public boolean logicalNegation(boolean value) {
        return !value;
    }

    public int bitwiseComplement(int x) {
        return ~x;
    }
}